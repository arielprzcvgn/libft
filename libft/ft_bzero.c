#include <string.h>

void	*ft_bzero(void *b, size_t len)
{
	size_t			i;
	unsigned char	*p;

	p = b;
	i = 0;
	while (i < len)
	{
		*(p + i) = 0;
		i++;
	}
	return (b);
}
