#include "libft.h"

void	ft_putnbr(int n)
{
	int		pow;

	if (n == -2147483648)
		ft_putstr("-2147483648");
	else
	{
		pow = 1;
		if (n < 0)
		{
			ft_putchar('-');
			n *= -1;
		}
		while (pow <= n / 10)
			pow *= 10;
		while (pow > 0)
		{
			ft_putchar((n / pow) + 48);
			n = n % pow;
			pow /= 10;
		}
	}
}
